#define MAX_FRAMES 9
int i_max_steps = 60;
int i_curr_steps = 0;
typedef struct _frame
{
	//Variables para GUARDAR Key Frames
	float posX;		//Variable para PosicionX
	float posY;		//Variable para PosicionY
	float posZ;		//Variable para PosicionZ
	float rotRodIzq;
	float giroMonito;

}FRAME;

FRAME KeyFrame[MAX_FRAMES];
int FrameIndex = 0;			//introducir número en caso de tener Key guardados
bool play = false;
int playIndex = 0;

void saveFrame(void)
{
	//printf("frameindex %d\n", FrameIndex);
	std::cout << "Frame Index = " << FrameIndex << std::endl;

	KeyFrame[FrameIndex].posX = posX;
	KeyFrame[FrameIndex].posY = posY;
	KeyFrame[FrameIndex].posZ = posZ;

	KeyFrame[FrameIndex].rotRodIzq = rotRodIzq;
	KeyFrame[FrameIndex].giroMonito = giroMonito;

	FrameIndex++;
}

void resetElements(void)
{
	posX = KeyFrame[0].posX;
	posY = KeyFrame[0].posY;
	posZ = KeyFrame[0].posZ;

	rotRodIzq = KeyFrame[0].rotRodIzq;
	giroMonito = KeyFrame[0].giroMonito;
}

void interpolation(void)
{
	incX = (KeyFrame[playIndex + 1].posX - KeyFrame[playIndex].posX) / i_max_steps;
	incY = (KeyFrame[playIndex + 1].posY - KeyFrame[playIndex].posY) / i_max_steps;
	incZ = (KeyFrame[playIndex + 1].posZ - KeyFrame[playIndex].posZ) / i_max_steps;

	rotInc = (KeyFrame[playIndex + 1].rotRodIzq - KeyFrame[playIndex].rotRodIzq) / i_max_steps;
	giroMonitoInc = (KeyFrame[playIndex + 1].giroMonito - KeyFrame[playIndex].giroMonito) / i_max_steps;

}

void animate(void)
{
	//myVar += 0.005f;

	//lightPosition.x = 80.0f * sin(myVar);
	//lightPosition.z = 80.0f * cos(myVar);
		
	//------------------------- Bloque de codigo para el cambio de color de luz ---------------------------------------------------
	conteoColor += 0.05f;

	if (conteoColor < 5)
	{
		cambiaColor = 1;
	}
	if (conteoColor >= 5 && conteoColor < 10)
	{
		cambiaColor = 2;
	}
	if (conteoColor >= 10 && conteoColor < 20)
	{
		cambiaColor = 3;
	}
	if (conteoColor >= 20)
	{
		conteoColor = 0.0f;
		cambiaColor = 1;
	}
		
	switch (cambiaColor)
	{
	case 1:
		Color2 = glm::vec3(1.0f, 0.5f, 0.0f);
		break;
	
	case 2:
		Color2 = glm::vec3(0.3f, 0.5f, 1.0f);
		break;

	case 3:
		Color2 = glm::vec3(0.96f, 0.0f, 0.52f);
		break;
	
	default:
		break;
	}
	//-----------------------------------------------------------------------------------------------------------------------------

	if (play)
	{
		if (i_curr_steps >= i_max_steps) //end of animation between frames?
		{
			playIndex++;
			if (playIndex > FrameIndex - 2)	//end of total animation?
			{
				std::cout << "Animation ended" << std::endl;
				//printf("termina anim\n");
				playIndex = 0;
				play = false;
			}
			else //Next frame interpolations
			{
				i_curr_steps = 0; //Reset counter
								  //Interpolation
				interpolation();
			}
		}
		else
		{
			//Draw animation
			posX += incX;
			posY += incY;
			posZ += incZ;

			rotRodIzq += rotInc;
			giroMonito += giroMonitoInc;

			i_curr_steps++;
		}
	}

	//Vehículo
	//------------------------------------------------- Animacion de desplazamiento del auto ----------------------------------------------------
	if (animacion)
	{
		if (!Edo1){ 
			movAuto_z -= 4.0f; 
			if (movAuto_z <= -170.0f) {
				Edo1 = true;
			}
		}
		
		if(Edo1 && !Edo2){
			movAuto_y += 4.0f;
			if (movAuto_y >= 200.5f){
				Edo2 = true;
			}
		}
		
		if (Edo1 && Edo2 && !Edo3){
			movAuto_z += 4.0f;
			if (movAuto_z >= 410.5f){
				Edo3 = true;
			}
		}
		
		if (Edo1 && Edo2 && Edo3 && !Edo4){
			movAuto_y -= 4.0f;
			if (movAuto_y <= 0.0f) {
				Edo4 = true;
			}
		}

		if (Edo1 && Edo2 && Edo3 && Edo4 && !Edo5) {
			movAuto_z += 4.0f;
			if (movAuto_z >= 710.0f){
				Edo5 = true;
				animacion = false;
			}
		}
	}
	//----------------------------------------------------------------------------------------------------------------------------------------------
}

